class SFA(nn.Module):
    def __init__(self, in_channel, out_channel):
        super(SFA, self).__init__()
        self.conv1 = nn.Conv3d(in_channel, out_channel, kernel_size=1)  ### F1
        self.conv2 = nn.Conv3d(in_channel, out_channel, kernel_size=1, dilation=3)  ### F2
        self.conv3 = nn.Conv3d(in_channel, out_channel, kernel_size=1, dilation=5)  ### F3

        self.conv4 = nn.Conv3d(out_channel * 2, out_channel, kernel_size=1)
        self.relu = nn.ReLU()
        self.conv5 = nn.Conv3d(out_channel, 1, kernel_size=1)
        self.softmax = nn.Softmax(dim=-1)

    def forward(self, x):
        x1 = self.conv1(x)  ### F1
        x2 = self.conv2(x)  ### F2
        x3 = self.conv3(x)  ### F3
        x12 = torch.cat((x1, x2), dim=1)  ### F12
        x23 = torch.cat((x2, x3), dim=1)  ### F23
        x12_ = self.conv4(x12)
        x12_ = self.relu(x12_)
        x23_ = self.conv4(x23)
        x23_ = self.relu(x23_)
        f12 = self.conv5(x12_)
        f23 = self.conv5(x23_)  ####1*1conv
        F12 = self.softmax(f12)
        F23 = self.softmax(f23)
        out1 = (F12 * x1) + (F12 * x2)
        out2 = (F23 * x2) + (F23 * x3)
        out = x + out1 + out2
        out = F.interpolate(out, size=x.size()[2:], mode='trilinear', align_corners=False)
        return out

from torch.nn.parameter import Parameter
class External_attention(nn.Module):
    '''
    Arguments:
        c (int): The input and output channel number.
    '''
    def __init__(self, c):
        super(External_attention, self).__init__()

        self.conv1 = nn.Conv3d(c, c, 1)
        self.k = 64
        self.linear_0 = nn.Conv1d(c, self.k, 1, bias=False)
        self.linear_1 = nn.Conv1d(self.k, c, 1, bias=False)
        self.linear_1.weight = Parameter(self.linear_0.weight.permute(1, 0, 2))
        self.conv2 = nn.Sequential(
            nn.Conv3d(c, c, 1, bias=False),
            nn.BatchNorm3d(c))
        self.relu = nn.ReLU()
        self.softmax = nn.Softmax(dim=-1)

    def forward(self, x):
        idn = x
        x = self.conv1(x)
        b, c, h, w, d = x.size()
        n = h * w * d
        x = x.view(b, c, n)  # b * c * n

        attn = self.linear_0(x)  # b, k, n
        attn = self.softmax(attn)  # b, k, n
        attn = attn / (1e-9 + attn.sum(dim=1, keepdims=True))  # b, k, n
        x = self.linear_1(attn)  # b, c, n
        x = x.view(b, c, h, w, d)
        x = self.conv2(x)
        x = x + idn
        x = self.relu(x)
        return x

class AFF(nn.Module):
    def __init__(self, in_channel):
        super(AFF, self).__init__()
        self.EA = External_attention(in_channel)
        self.sig = nn.Sigmoid()
        self.conv = nn.Conv3d(in_channel, 1, kernel_size=1)

    def forward(self, xh, g):  ## x high, g low
        xh = F.interpolate(xh, size=xh.size()[2:], mode='trilinear', align_corners=False)
        x = torch.cat((xh, g), dim=1) 
        att = self.EA(x)
        x = x*att
        x = self.conv(x)
        x = torch.mean(x, dim=0, keepdim=True)
        x = self.sig(x)
        out1 = x*g
        out = out1 + xh
        return out

class Upsample(nn.Module):
    def __init__(self, size=None, scale_factor=None, mode='nearest', align_corners=False):
        super(Upsample, self).__init__()
        self.align_corners = align_corners
        self.mode = mode
        self.scale_factor = scale_factor
        self.size = size

    def forward(self, x):
        return nn.functional.interpolate(x, size=self.size, scale_factor=self.scale_factor, mode=self.mode,
                                         align_corners=self.align_corners)

